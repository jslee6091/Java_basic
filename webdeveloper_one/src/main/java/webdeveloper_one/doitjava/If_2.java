package webdeveloper_one.doitjava;

public class If_2 {

	public static void main(String[] args) {
		int one = 10, two = 20;
		
		if(one > two) {
			System.out.println("one > two");
		}
		
		if(one < two) {
			System.out.println("one < two");
		}
		
		if(one == two) {
			System.out.println("one == two");
		}
		
	}

}
