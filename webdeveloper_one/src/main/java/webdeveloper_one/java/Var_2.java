package webdeveloper_one.java;

public class Var_2 {

	public static void main(String[] args) {

		// 주석
		/*
		 * 멀티 주석
		 * 
		 * 
		 * 
		 */

		// 주석 단축키 : ctrl + shift + / 또는 ctrl + shift + c

		// 정수형변수
		int age = 10;

		// 오렌지 색으로 나오는 것들은 모두 예약어이다.
		// 예약어는 변수명, 메소드명으로 사용 불가
		age = 20;
		System.out.println("나이는 : " + age);
		System.out.println(age + 10);
		System.out.println(age - 10);

		age = age + 10;
		age = age - 10;
		System.out.println("나이는 : " + age);
	}

}
