package webdeveloper_one.java;

public class Opt_1 {

	public static void main(String[] args) {
		int init = 0;
		// ++변수 : 실행되면 1 증가
		// 변수++ : 실행하고 나서 다시 실행되면 1 증가

		System.out.println(init++);
		System.out.println(++init);
		System.out.println(init);
		System.out.println(init++);
		System.out.println(init++);
		System.out.println(init);
	}

}
